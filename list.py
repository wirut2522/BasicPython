# การสร้าง list ใน python
numbers = [20,32,89,95,130]
names = ['john','joe','jim','jane']
mixed = [-2,5,84,2,"Mountain","Python"]

# การแสดงข้อมูลใน list
print(numbers[0])
print(numbers[3])
print(names[3])
print(mixed[4])

# การวน loop

for num in numbers:  # ขวาไปซ้าย
    print(num)

for name in names:
    print(name)

for mix in mixed:
    print(mix)
    